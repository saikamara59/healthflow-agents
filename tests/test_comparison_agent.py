def make_agent(response_text: str = "Recommendation", **kwargs) -> tuple[ComparisonAgent, MagicMock]:
    """Agent with a mocked Anthropic client returning `response_text`."""
    mock_client = MagicMock()
    mock_response = MagicMock()
    mock_response.content = [MagicMock(text=response_text)]
    mock_client.messages.create.return_value = mock_response
    return ComparisonAgent(client=mock_client, **kwargs), mock_client


class RecordingAuditSink:
    def __init__(self) -> None:
        self.events: list[tuple[str, dict]] = []

    def log(self, event_type: str, details: dict) -> None:
        self.events.append((event_type, details))


class RecordingInvocationTracker:
    def __init__(self) -> None:
        self.records: list[MagicMock] = []
        self.calls: list[dict] = []

    @contextmanager
    def __call__(self, *, agent: str, event_type: str, model: str | None = None):
        self.calls.append({"agent": agent, "event_type": event_type, "model": model})
        record = MagicMock()
        record.details = {}
        self.records.append(record)
        yield record


# --- Golden smoke tests: identical prompt strings vs. the original ---


def test_system_prompt_matches_original_verbatim():
    agent, _ = make_agent()
    assert agent.system_prompt == GOLDENS["system_prompt"]


def test_build_prompt_matches_original_verbatim_with_meds_and_procedures():
    agent, _ = make_agent()
    prompt_input = ComparisonPromptInput(
        plans=SAMPLE_PLANS,
        age=65,
        income_level="low",
        medications=["Metformin"],
        procedures=["MRI"],
    )
    assert agent._build_prompt(prompt_input) == GOLDENS["user_prompt_full"]


def test_build_prompt_matches_original_verbatim_without_meds_or_procedures():
    agent, _ = make_agent()
    prompt_input = ComparisonPromptInput(
        plans=SAMPLE_PLANS, age=65, income_level="low", medications=[], procedures=[]
    )
    assert agent._build_prompt(prompt_input) == GOLDENS["user_prompt_bare"]


def test_recommend_sends_golden_prompts_and_returns_text_output():
    agent, mock_client = make_agent("Plan A is the best choice for your budget.")
    result = agent.recommend(
        plans=SAMPLE_PLANS,
        age=65,
        income_level="low",
        medications=["Metformin"],
        procedures=["MRI"],
    )

    kwargs = mock_client.messages.create.call_args.kwargs
    assert kwargs["system"] == GOLDENS["system_prompt"]
    assert kwargs["messages"] == [
        {"role": "user", "content": GOLDENS["user_prompt_full"]}
    ]
    assert kwargs["model"] == "claude-haiku-4-5"
    assert kwargs["max_tokens"] == 1024
    # Output shape: plain string, same as the original agent.
    assert isinstance(result, str)
    assert result == "Plan A is the best choice for your budget."


