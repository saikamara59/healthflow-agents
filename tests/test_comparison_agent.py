def test_agent_stubs_anthropic_call_in_test_mode(monkeypatch):
    """In HEALTHFLOW_TEST_MODE, recommend() must NOT call the Anthropic API.

    The e2e docker stack runs with a fake API key; without a stub path,
    /compare returns 500 because the API rejects the key.
    """
    monkeypatch.setenv("HEALTHFLOW_TEST_MODE", "1")
    agent, mock_client = make_agent()
    result = agent.recommend(plans=SAMPLE_PLANS, age=65, income_level="low")

    assert isinstance(result, str) and result
    assert result == _TEST_MODE_RECOMMENDATION
    mock_client.messages.create.assert_not_called()


# --- Injection pattern: audit + invocation contracts preserved ---


def test_injected_sinks_receive_original_event_stream():
    audit = RecordingAuditSink()
    tracker = RecordingInvocationTracker()
    agent, _ = make_agent("Some recommendation", audit_sink=audit, invocation_tracker=tracker)

    agent.recommend(plans=SAMPLE_PLANS, age=65, income_level="low")

    assert [e for e, _ in audit.events] == ["tool_called", "recommendation_generated"]
    assert audit.events[0][1] == {"tool": "claude_api", "model": "claude-haiku-4-5"}
    assert audit.events[1][1] == {"length": len("Some recommendation")}
    assert tracker.calls == [
        {"agent": "comparison", "event_type": "recommend", "model": "claude-haiku-4-5"}
    ]
    assert tracker.records[0].details == {
        "length": len("Some recommendation"),
        "plans": 2,
    }


def test_test_mode_stub_audit_payload_preserved(monkeypatch):
    monkeypatch.setenv("HEALTHFLOW_TEST_MODE", "1")
    audit = RecordingAuditSink()
    tracker = RecordingInvocationTracker()
    agent, _ = make_agent(audit_sink=audit, invocation_tracker=tracker)

    agent.recommend(plans=SAMPLE_PLANS, age=65, income_level="low")

    assert audit.events == [
        (
            "recommendation_generated",
            {"length": len(_TEST_MODE_RECOMMENDATION), "stubbed": True},
        )
    ]
    assert tracker.records[0].details == {
        "length": len(_TEST_MODE_RECOMMENDATION),
        "stubbed": True,
    }


